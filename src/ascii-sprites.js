/**
 * Render a single animation frame to an array of display lines.
 * @param {string} species   species key (e.g. 'duck')
 * @param {number} frameIdx  0=idle, 1=fidget, 2=action
 * @param {string} eyeChar   single char (e.g. '·')
 * @param {string} hat       hat name (e.g. 'none', 'crown')
 * @returns {string[]}       trimmed lines ready to join with \n
 */
export function renderFrame(species, frameIdx, eyeChar, hat = 'none') {
  const speciesFrames = SPRITES[species] ?? SPRITES.duck;
  const frame = speciesFrames[frameIdx] ?? speciesFrames[0];

  // Replace {E} placeholders with eye char
  let lines = frame.map(l => l.replace(/\{E\}/g, eyeChar));

  // Apply hat overlay on line 0 if hat is not 'none'
  if (hat && hat !== 'none') {
    lines[0] = HATS[hat] ?? HATS.none;
  }

  // Drop line 0 if: no hat AND this frame's line 0 is blank
  // Per-frame check (not all-frames) so idle frames normalize to 4 lines regardless of species,
  // while action frames with decoration (dragon ~, octopus o, etc.) keep their 5th line.
  const noHat = !hat || hat === 'none';
  if (noHat && lines[0].trim() === '') {
    lines = lines.slice(1);
  }

  return lines;
}
